var botao = document.getElementById("btn");
botao.addEventListener('click', function (mostraMsg) {
    mostraMsg = prompt("Você quer jogar?\nDigite sim ou não")
    if (mostraMsg.toLowerCase() == "sim") {
        alert("Você deve tomar decisões sobre a situação que o personagem escolhido se encontra, informando a sua opção de escolha através dos prompts na tela, digitando sim ou não.\nSerá que nossos laxcados se sairão bem?")
        return location = "./escolhaDePersonagem.html"
    } else if (mostraMsg.toLowerCase() == "não" || mostraMsg.toLowerCase() == "nao") {
        alert("Parabéns você perdeu a oportunidade de salvar um laxcado!")
    } else {
        alert("Essa opção não foi encontrada, tente novamente.")
    }
})

